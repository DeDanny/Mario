package mario.core;

/**
 *
 * @author Danny
 */
public class Node {
    protected int x, y;
    public Node(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }


}
